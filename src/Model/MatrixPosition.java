package Model;

public class MatrixPosition {

	public MatrixPosition(int x, int y) {
		super();
		X = x;
		Y = y;
	}
	int X;
	public int getX() {
		return X;
	}
	public void setX(int x) {
		X = x;
	}
	public int getY() {
		return Y;
	}
	public void setY(int y) {
		Y = y;
	}
	int Y;
	
}
